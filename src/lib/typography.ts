/** Неразрывный пробел (U+00A0). */
const NBSP = " ";

/**
 * Короткие служебные слова (предлоги, союзы, частицы), которые по правилам
 * русской типографики нельзя оставлять висеть в конце строки.
 */
const SHORT_WORDS = new Set([
  "а", "и", "о", "у", "в", "к", "с", "я", "во", "со", "об", "ко", "на", "за",
  "по", "из", "от", "до", "не", "ни", "же", "бы", "ли", "то", "но", "да", "как",
  "для", "что", "или", "чем", "под", "над", "при", "без", "про", "мы", "вы",
  "он", "их", "её", "его", "дк",
]);

/**
 * Убирает висячие предлоги/союзы: приклеивает короткие служебные слова, числа и
 * слово перед тире к следующему слову неразрывным пробелом (U+00A0), чтобы они
 * не оставались в конце строки при переносе.
 */
export function preventOrphans(text: string): string {
  const words = text.split(" ");
  let out = "";
  for (let i = 0; i < words.length; i++) {
    out += words[i];
    if (i === words.length - 1) break;

    const bare = words[i].replace(/[.,!?;:—()«»"'*]/g, "").toLowerCase();
    const isNumber = /^\d+$/.test(bare);
    const nextIsDash = /^—/.test(words[i + 1]);

    out += SHORT_WORDS.has(bare) || isNumber || nextIsDash ? NBSP : " ";
  }
  return out;
}
